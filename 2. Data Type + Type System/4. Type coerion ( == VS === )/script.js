// Type Coercion in JavaScript

// Type coercion is the process of converting a value from one type to another. In JavaScript, this can happen implicitly (type conversion) or explicitly (type casting).

"5" + 1; // '51' (string concatenation)
"5" - 1; // 4 (number subtraction)

true + 1; // 2 (boolean to number)
false + 1; // 1 (boolean to number)
