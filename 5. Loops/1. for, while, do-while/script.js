// for, while, do-while

// kahase jaana hai -> kaha tak jaana hai -> kaise jaana hai
// for loop
for (start; end; increment/decrement) {
    // code to be executed
}
//Example
for (let i = 0; i < 5; i++) {
    console.log(i);
} //Output: 0, 1, 2, 3, 4

// kahase jaana hai -> kab rukna hai -> kaise jaana hai
// while loop
let start;
while (end) {
    // code to be executed
}
//Example
let j = 0;
while (j < 5) {
    console.log(j);
    j++;
} // Output: 0, 1, 2, 3, 4


// do-while loop
//Example
let k = 0;
do {
    console.log(k);
    k++;
} while (k < 5); //Output: 0, 1, 2, 3, 4

