// Reassignment and Re-declaration in JavaScript

// var
var a = 10;
a = 20; // Reassignment is allowed with var

var d = 70;
var d = 80; // Re-declaration is allowed with var

// let
let b = 30;
b = 40; // Reassignment is allowed with let

let e = 90;
// let e = 100; // This would throw an error - re-declaration not allowed with let

// const
const c = 50;
// const c = 60; // This would throw an error - reassignment not allowed with const

const f = 110;
// const f = 120; // This would throw an error - re-declaration not allowed with const